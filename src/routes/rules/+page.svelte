<main class="bg-gray-50 px-4 py-4 sm:px-6 lg:px-8">
	<div class="mx-auto max-w-4xl rounded-xl bg-white p-8 shadow-md sm:p-10">
		<p class="mb-10 text-center text-gray-500">
			Please read the following rules carefully to ensure fair play and the smooth running of the
			league.
		</p>

		<ol class="list-outside list-decimal space-y-5 pl-5 leading-relaxed text-gray-700">
			<li>
				The League shall be known as the Chester and District Tennis League (CADSTL) and
				administered by a Committee consisting of a Chairperson, Secretary, Treasurer and a League
				Organiser for each Section. Three to form a quorum.
			</li>
			<li>
				The League shall be open to teams in the district, subject to acceptance by the Committee.
			</li>
			<li>
				The League shall have four Sections, Men’s, Ladies, Mixed and Men’s Veterans. The Men’s
				Veterans Section will be for players aged 55 in the season calendar year, or older. The
				Committee has the discretion to split the Sections into appropriate Divisions depending upon
				the number of teams entered in a particular season. Teams can only drop into lower leagues
				through relegation, not as a result of losing players. If a club with more than one team in
				a section withdraws a team, it must withdraw the lowest placed team.
			</li>
			<li>
				Each team shall consist of four players:
				<ul class="mt-3 list-outside list-disc space-y-2 pl-6">
					<li>Men’s Divisions - 4 men players.</li>
					<li>Mixed Divisions - 2 ladies and 2 men.</li>
					<li>Ladies Divisions - 4 lady players.</li>
				</ul>
			</li>
			<li>
				All players must be bona-fide club members and no players shall play for more than one Club
				in any one of the Sections per season. Any rubbers played by an ineligible player will be
				awarded to the opposition team.
			</li>
			<li>Players selected must play in order of merit.</li>
			<li>
				For Clubs with more than one team in any of the Sections, any player having played on three
				occasions for any higher team, may not play subsequently for a lower team.
			</li>
			<li>
				A match shall consist of four doubles rubbers and each rubber is the best of three sets. A
				mixed match now consists of four mixed rubbers. A 7 point tie-break (i.e. the team who first
				wins 7 points shall win the tie-break, provided there is a margin of 2 points) will come
				into operation at 6 games all in the 1st and 2nd sets.
			</li>
			<li>
				If the score is one set all after the first two sets, a 10 point tie-break is the deciding
				final set. The first to get to 10 points is the winner, provided there is a margin of 2
				points. To enter this on the online system, please record as a 1-0, or 0-1, as applicable.
			</li>
			<li>
				One point will be awarded for every set won (including the 3rd tie-break set). Should teams
				finish level on points/sets at the end of the season, placings will be decided on the basis
				of sets difference, and if still level, on games difference (3rd set tie-breaks count as one
				game).
			</li>
			<li>
				Matches must be played on the date stated in the fixture list. In the case of inclement
				weather, the Captains may agree to postpone the match. In this event games should be
				rearranged as soon as possible. The home Captain should offer three alternative dates
				(excluding dates where the away team has another CADSTL fixture) and inform the League
				organiser of the agreed date. Failure to agree should be referred to the League Organiser.
			</li>
			<li>
				Teams unable to fulfil a fixture (not weather related) will forfeit the match unless both
				teams agree to replay at a later date. Team captains should notify the League Organiser of
				any postponement within 1 week of the original fixture date. If teams agree to play the
				non-defaulting team shall offer three alternative dates. Re-arranged matches must be played
				as soon as possible. If the postponement is in the first half of the season the match must
				be played before the reverse fixture in the second half of the season. If the match is not
				played on these dates the defaulting team shall forfeit the match and will be penalised by
				having 8 points deducted, the non-defaulting team will be awarded 8 points. It is the
				responsibility of the non-defaulting team to record the result on the online system and
				notify the appropriate League Organiser.
			</li>
			<li>
				For evening matches play will continue, where necessary, for up to 30 minutes after sunset
				unless all four players are in agreement to continue after this time.
			</li>
			<li>
				Two rubbers must be completed otherwise the match must be replayed on a date agreed by both
				teams.
			</li>
			<li>
				If after two completed rubbers a match is abandoned due to inclement weather, the remaining
				two rubbers will be halved. Games recorded in the online system as 6-0, 0-6 for each rubber.
			</li>
			<li>
				If a set is completed then that set’s game score should be recorded. The remaining set will
				be halved with 0.5 points awarded to each team. Games in sets that are not completed should
				not be recorded.
			</li>
			<li>
				The home captain should enter the result in the online system as soon as possible but within
				1 week of the completion of the match. It is the away team captain’s responsibility to check
				if the results have been entered correctly and discuss with the home captain or notify the
				League Organiser as appropriate. Teams that regularly fail to enter their results shall be
				liable to loss of all points available, at the discretion of the Committee.
			</li>
			<li>
				Evening matches should commence at 6:15 p.m. Weekend matches to commence at the starting
				time stated in the fixture list issued at the beginning of each season. No matches on a
				Saturday evening. Any players not arriving within 30 minutes of the start time shall forfeit
				their first rubber. For the non-arrival of a player within a further 30 minutes, the
				offending team shall forfeit both rubbers.
			</li>
			<li>
				Veterans matches will commence on a Monday to Friday between 10am and 2pm on a day selected
				by the home club.
			</li>
			<li>
				For evening matches floodlights are permitted if the away team agrees to the use of them.
				Such agreement should be sought before the match commences. Some clubs have time
				restrictions on floodlight use. The home team captain must inform the away team of any
				restrictions before the match commences.
			</li>
			<li>
				The home club will provide a minimum of three new L.T.A. approved balls per first round
				rubber and have some reasonable spare balls available.
			</li>
			<li>
				Courts should be clearly marked with white lines, and the only permanent lines on court
				should be for tennis.
			</li>
			<li>
				Teas are not required from the home club (unless agreed in advance by both Captains), though
				normal hospitality from the home club is expected.
			</li>
			<li>
				The affiliation fee, at the rate agreed by the Annual General Meeting, is to be paid by the
				28th February for the following season.
			</li>
			<li>
				The Committee shall be the absolute and sole authority for the interpretation of these rules
				and their decision shall be final and binding on all clubs.
			</li>
			<li>
				An Annual General Meeting of the League will be held before the end of November each year at
				a date to be fixed by the Committee.
			</li>
			<li>
				The Annual General Meeting shall:
				<ol class="mt-3 list-outside list-[lower-alpha] space-y-2 pl-6">
					<li>Receive reports for the year and adopt accounts</li>
					<li>Elect officers</li>
					<li>Agree subscriptions for the ensuing season</li>
					<li>Present Trophies to the League Winners</li>
					<li>
						Consider any notices of motion (such notices of motion must be made in writing and
						received by the League Secretary by the 1st September).
					</li>
				</ol>
			</li>
			<li>
				A quorum for any General Meeting shall be representative of at least 50% of the member
				clubs. Each club represented is entitled to one vote. The Chairperson is entitled to a
				casting vote.
			</li>
			<li>
				Registrations and applications by new teams must be received in writing by 28th February for
				the following season.
			</li>
			<li>
				Any dispute arising on any matter connected with the League shall be reported to the
				Secretary in writing. The Committee shall consider such disputes and their decision is
				final.
			</li>
		</ol>
	</div>
</main>
